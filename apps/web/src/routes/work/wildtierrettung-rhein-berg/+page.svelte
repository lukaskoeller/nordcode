<h2>Was war das Ziel?</h2>
<p>Die Ziele waren übersichtlich, aber auch relativ vage:</p>
<ol>
	<li>
		Erstellt eine Webseite, mit der wir uns sehen lassen können, mit Fokus auf folgende Punkte
		<ul>
			<li>Der Verein, seine Aufgaben und Akitvitäten sollen vorsgestellt werden</li>
			<li>Es soll gezeigt werden, wie man helfen kann (Spenden und Freiwilligenarbeit)</li>
			<li>
				Landwirt:innen sollen über die Vorteile informiert werden und es soll ihnen einfach
				gemacht werden, die Wildtierrettung zu beauftragen
			</li>
		</ul>
	</li>
	<li>Sie brauchten eine neue E-Mail-Adresse</li>
	<li>Die Webseite sollte eine Kontaktform haben</li>
</ol>
<p>
	Dazu musste die Webseite betextet werden und wir sollten auch ein bisschen SEO machen, damit man
	die Webseite auch findet. Konkretes dazu unter <a href="#seo">SEO und Content</a>.
</p>
<p>
	Bei Design hatten wir relativ freie Hand. Es gab ein Logo und einen Instagram Account, woran wir
	uns orientiert haben. Mehr Details dazu unter <a href="#design">Design</a>.
</p>

<h2>Was haben wir erreicht?</h2>
<p>
	Da es keine konkreten Ziele gab, ist es schwer konkrete Zahlen zu nennen, aber hier ein paar
	Punkte:
</p>
<p>
	Wir haben eine Webseite geschaffen, die dem Verein gefällt und die er gerne teilt. Sie war
	rechtzeitig zur Start der Saison fertig und hat den Verein in seiner Arbeit unterstützt.
</p>
<p>
	Dafür haben wir die <strong
		>Kontaktaufnahme durch sinnvoll positionierte CTAs, Emails und Kontaktformulare einfach</strong
	> gemacht.
</p>

<figure>
	<enhanced:img class="img" src={header} />
	<figcaption>
		Klare Strukturen, einfache Navigation ein CTA, um direkt zur Anmeldung zu kommen.
	</figcaption>
</figure>

<p>
	Auch <strong>auf dem Handy ist die Webseite sehr einfach zu bedienen</strong>. Den Fokus haben
	wir darauf gelegt, dass man schnell findet, wonach man sucht oder was relevant ist,
	<strong>indem wir die Besucher:innen an die Hand nehmen und führen</strong>.
</p>
<p>
	Durch <strong
		>keyword-recherche-basierten Content kann man die Webseite gut auf Google und co finden</strong
	>. Zur Unterstüzung haben wir den Verein noch auf Google my Business angemeldet auf diversen
	Wildtierrettungs-Registern eingetragen.
</p>

<h2 id="design">Umsetzung: Design</h2>
<p>
	Wie beschrieben, waren die Design Vorgaben gering. Das Design sollte <strong
		>aufgeräumt, modern und klar</strong
	>
	sein. Dabei sollte die Webseite nicht zu verspielt, aber auch nicht trocken oder klinisch wirken
	- eben ein guter Mix für einen Verein, der Wildtiere rettet. Wichtig ist, dass
	<strong>wir hier nicht nur internetaffine Menschen ansprechen</strong> und wir darauf achten mussten,
	dass auch diese sich gut zurecht finden.
</p>
<h3>Farben</h3>
<p>
	Für die Farben haben wir uns am Logo orientiert und daraus abgeleitet Varianten entworfen. Da
	wir verschiedene Zielgruppen auf der Seite bedienen, konnten wir mit den Farben unterstützen und
	die Navigation und Wiedererkennung etwas erleichtern.
</p>

<figure>
	<enhanced:img class="img" src={verteiler} />
	<figcaption>
		Durch die Farben haben wir die Bereiche getrennt. Auf den Detailseiten findet man sie direkt
		wieder und weiß, dass man richtig ist.
	</figcaption>
</figure>

<p>
	Die Farben siedeln sich natürlich in den naturnahen Wald- und Wiesenfarben an, die auch im Logo
	zu finden sind. Das macht die Webseite freundlich und einladend, vermittelt aber auch gleich das
	Gefühl, dass es um dieses Thema geht.
</p>
<p>
	<strong
		>Dabei haben wir natürlich stets wert darauf gelegt, dass die Farben barrierefrei sind und
		eine gute Lesbarkeit gewährleisten.</strong
	>
</p>

<h3>Struktur</h3>

<p>
	Auch bei der Struktur lag der <strong>Fokus auf Klarheit</strong>. Mit einer angenehmen
	Schriftgröße, ausreichend Abstand und klaren Blöcken entsteht eine leicht lesbare und auch
	scanbare Seite.
</p>
<p>
	Durch <strong
		>viele exzellente Bilder<strong
			>, die wir vom Verein bekommen haben, haben wir die Seite aufgelockert. Dabei kann man
			auf den Bildern die Mitglieder:innen und die Tiere sehen und somit der Verein gut
			präsentiert.</strong
		></strong
	>
</p>
<p>
	Zusätzlich haben wir mit Zahlenblöcken eine gute Übersicht über die Arbeit des Vereins
	geschaffen erstellt und mit Zitaten der Mitglieder:innen die persönliche Ebenen hervorgehoben.
</p>

